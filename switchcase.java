public class switchcase{
    public static void main(String args[]){
        char ch = 'b';
        switch(ch) {
            case 'b' : System.out.println("samosa");
            break;
            case 2 : System.out.println("pizza");
            break;
            case 3 : System.out.println("burger");
            break;
            default : System.out.println("we wake up");
            
            


        }

    }
}